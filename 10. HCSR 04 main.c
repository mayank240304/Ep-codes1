/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
unsigned int echo_time;
float distance;
unsigned char buf[16];

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* Configure the system clock */
  SystemClock_Config();
  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_TIM6_Init();
  HAL_TIM_Base_Start(&htim6);
  lcd_init();
  lcd_clear();
  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
	  lcd_displayString(1,1,"HC-SR04");
	  HAL_Delay(100);
	  while((HAL_GPIO_ReadPin (ECHO_GPIO_Port, ECHO_Pin)==1));
	  //give rig pulse
	  HAL_GPIO_WritePin (TRIG_GPIO_Port, TRIG_Pin, 1);   // pull the pin high
	  delay(10);
	  HAL_GPIO_WritePin (TRIG_GPIO_Port, TRIG_Pin, 0);   // pull the pin low

	  //check echo signal going high
	  while((HAL_GPIO_ReadPin (ECHO_GPIO_Port, ECHO_Pin)==0));

	  //start counting ECHO high time
	  __HAL_TIM_SET_COUNTER(&htim6, 0);
	  while((HAL_GPIO_ReadPin (ECHO_GPIO_Port, ECHO_Pin)==1));
	  echo_time = __HAL_TIM_GET_COUNTER(&htim6);

	  if(echo_time >= 38000)
	  {
		  lcd_displayString(2,0,"no signal");
	  }
	  else
	  {
		  lcd_clear();
		  distance = (float)(echo_time*0.0343)/2;
		  sprintf(buf,"%3.2f cm",distance);
		  lcd_displayString(2,0,buf);
	  }
  }
  /* USER CODE END 3 */
}

